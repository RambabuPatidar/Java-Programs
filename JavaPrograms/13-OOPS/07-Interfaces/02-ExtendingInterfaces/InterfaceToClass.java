package com.OOPS.Interfaces.ExtendingInterfaces;

public class InterfaceToClass {
    int a;
    String name;
    int age;

   public InterfaceToClass() {
       // default constructor
   }

    public InterfaceToClass(int a, String name, int age) {
        this.a = a;
        this.name = name;
        this.age = age;
    }
}
